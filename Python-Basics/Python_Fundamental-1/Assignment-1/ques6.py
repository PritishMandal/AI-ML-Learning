a = int(input("Enter first number: "))
b = int(input("Enter second number: "))

print("Before Swapping:")
print("a =", a)
print("b =", b)

# swapping
a, b = b, a

print("After Swapping:")# yeh line swapping ke baad print karna hai taki pata chale ki swap hua hai ya nahi
print("a =", a)
print("b =", b)