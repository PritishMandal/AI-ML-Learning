# Integer
age = 21
print("Integer:", age)
print(type(age))

# Float
price = 99.99
print("\nFloat:", price)
print(type(price))

# String
name = "Pritish"
print("\nString:", name)
print(type(name))

# Boolean
is_student = True
print("\nBoolean:", is_student)
print(type(is_student))

# None
data = None
print("\nNone:", data)
print(type(data))